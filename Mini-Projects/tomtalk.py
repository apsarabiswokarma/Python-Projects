def tom_talk(text):
    print(f"Tom: {text}")


# make Tom say some things
tom_talk("Hello, how are you?")
tom_talk("I'm doing well, thanks for asking.")
tom_talk("I'm just a computer program, so I don't have feelings or emotions like a human.")

# This code defines a function called tom_talk that takes a piece of text as
# an argument and prints it out with the prefix "Tom: ".
# To make Tom say some things, we call the tom_talk function with different pieces of text as arguments.
# This will print out the text preceded by "Tom: ".
